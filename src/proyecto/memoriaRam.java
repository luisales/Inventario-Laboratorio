    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ResultSet rx = Conectar.getTabla("select * from memoriaram where codigoRam='"+this.txtCodigo.getText()+"'");
        try {
            if(rx.next())
            {
                this.txtCapacidad.setText(rx.getString("capacidadRam"));
                this.txtCodigoM.setText(rx.getString("codigoMarca"));
                this.txtCodigoCPU.setText(rx.getString("codigoCpu"));
            }

        } catch (SQLException ex) {
            Logger.getLogger(memoriaRam.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

