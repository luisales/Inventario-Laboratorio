    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        if(txtCapacidad.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Llene el campo capacidad");
            return;
        }
        
         if(this.txtCodigoM.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Llene el campo Codigo Marca");
            return;
        }
         
          if(this.txtCodigoCPU.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Llene el campo CodigoCPU");
            return;
        }

        Conectar con=new Conectar();
        Connection reg=con.getConnection();

        try
        {
            
                PreparedStatement obj=reg.prepareStatement("INSERT INTO memoriaram(capacidadRam, codigoMarca, codigoCpu) values(?,?,?)");
                obj.setString(1,this.txtCapacidad.getText().toUpperCase());
                obj.setString(2,this.txtCodigoM.getText().toUpperCase());
                obj.setString(3,this.txtCodigoCPU.getText().toUpperCase());
                obj.executeUpdate();
                JOptionPane.showMessageDialog(null, "Guardado con exito");

                this.txtCapacidad.setText("");
                this.txtCodigoM.setText("");
                this.txtCodigoCPU.setText("");
            

        }
        catch(SQLException ex)
        {
            Logger.getLogger(memoriaRam.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        Conectar con=new Conectar();
        Connection reg=con.getConnection();
        try
        {
            PreparedStatement obj=reg.prepareStatement("UPDATE memoriaram SET capacidadRam='"+this.txtCapacidad.getText().toUpperCase()+"',codigoMarca='"+this.txtCodigoM.getText().toUpperCase()+"',codigoCpu='"+this.txtCodigoCPU.getText().toUpperCase()+ "'WHERE codigoRam='"+this.txtCodigo.getText()+"'");
            obj.executeUpdate();
            JOptionPane.showMessageDialog(null, "Guardado con exito");
        }
        catch(SQLException ex)
        {
            Logger.getLogger(memoriaRam.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

