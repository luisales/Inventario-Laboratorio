    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        if(txtClase.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Llene el campo nombre");
            return;
        }
        
        Conectar con=new Conectar();
        Connection reg=con.getConnection();

        try
        {
            
                PreparedStatement obj=reg.prepareStatement("INSERT INTO clase(nombreClase) values(?)");
                obj.setString(1,this.txtClase.getText().toUpperCase());
                obj.executeUpdate();
                JOptionPane.showMessageDialog(null, "Guardado con exito");
                this.txtClase.setText("");
            

        }
        catch(SQLException ex)
        {
            Logger.getLogger(clase.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        Conectar con=new Conectar();
        Connection reg=con.getConnection();
        try
        {
            PreparedStatement obj=reg.prepareStatement("UPDATE clase SET nombreClase='"+this.txtClase.getText().toUpperCase()+ "'WHERE codigoClase='"+this.txtCodigo.getText()+"'");
            obj.executeUpdate();
            JOptionPane.showMessageDialog(null, "Guardado con exito");
        }
        catch(SQLException ex)
        {
            Logger.getLogger(clase.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

