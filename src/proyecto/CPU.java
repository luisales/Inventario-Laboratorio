    private void txtram1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtram1KeyTyped
         char validar=evt.getKeyChar();
        
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
        }
        else if((int)validar>32 && (int)validar<=47
                ||(int)validar>58 && (int)validar<=64
                ||(int)validar>91 && (int)validar<=96
                ||(int)validar>44 && (int)validar<=47)
        {
            getToolkit().beep(); 
               
            evt.consume(); 
            
            
              
        }
    }//GEN-LAST:event_txtram1KeyTyped

